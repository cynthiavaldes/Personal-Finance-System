public class overallAmount {
    static float Amount;
    static float Original;
    static int AmtCount;
    
    public static void setOverallAmount(float amt){
        Amount = amt;
    }
    
    public static float getOverallAmount(){
        return Amount;
    }
    
    public static void setOriginalAmount(float org){
        Original = org;
    }
    
    public static float getOriginalAmount(){
        return Original;
    }
    
    public static void setAmtCount(int count){
        AmtCount = count;
    }
    
    public static float getAmtCount(){
        return AmtCount;
    }
}
